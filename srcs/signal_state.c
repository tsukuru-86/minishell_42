#include "../minishell.h"

/* 
 * グローバル変数：受け取ったシグナル番号を格納
 * 仕様に従って、シグナル番号のみを保持
 */
int	g_signum = 0;

/* 
 * シグナル番号を取得する関数
 */
int	get_signal_num(void)
{
	return (g_signum);
}

/* 
 * シグナル番号を設定する関数
 */
void	set_signal_num(int signum)
{
	g_signum = signum;
}
